package com.ecc.entity;
import java.math.BigDecimal;

public class Point {

		BigDecimal pointX;
		BigDecimal pointY;
		
		public BigDecimal getPointX() {
			return pointX;
		}
		public void setPointX(BigDecimal pointX) {
			this.pointX = pointX;
		}
		public BigDecimal getPointY() {
			return pointY;
		}
		public void setPointY(BigDecimal pointY) {
			this.pointY = pointY;
		}
		
		
		
		
}
